"""Cookie import / export in multiple formats.

Supported formats:
  - **Netscape** ``cookies.txt`` — universal format used by curl/wget/most
    antidetect tools, including AdsPower's "Import cookies" UI.
  - **Playwright/Chrome DevTools JSON** — list of cookie dicts matching
    ``browser_context.add_cookies()`` schema.
  - **AdsPower ``.adb`` profile directory** — bundled tarball/folder
    containing ``Default/Cookies`` (SQLite) + ``Local Storage`` etc.

Output: a normalised list of cookie dicts ready for ``add_cookies``.

Netscape format reference:
    # Netscape HTTP Cookie File
    # https://curl.haxx.ru/rfc/cookie_spec.html
    # This file was generated by libcurl! Edit at your own risk.

    domain  flag  path  secure  expiration  name  value

    domain   TRUE   /      FALSE  9999999999  foo   bar
"""
from __future__ import annotations

import json
import sqlite3
import tarfile
import zipfile
from dataclasses import asdict, dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional, Union


@dataclass
class Cookie:
    name: str
    value: str
    domain: str = ""
    path: str = "/"
    expires: float = -1  # -1 = session cookie (matches Playwright)
    http_only: bool = False
    secure: bool = False
    same_site: str = "Lax"  # "Strict" | "Lax" | "None"

    def to_playwright(self) -> Dict[str, Any]:
        out: Dict[str, Any] = {
            "name": self.name,
            "value": self.value,
            "path": self.path,
            "expires": self.expires,
            "httpOnly": self.http_only,
            "secure": self.secure,
            "sameSite": self.same_site,
        }
        # Playwright wants "url" OR "domain"+"path". We always pass domain.
        # Domain may include a leading dot (Netscape). Playwright accepts that.
        if self.domain:
            out["domain"] = self.domain
        return out


# ---------------------------------------------------------------------------
# Normalisation helpers
# ---------------------------------------------------------------------------


def _normalize_samesite(s: Optional[str]) -> str:
    if not s:
        return "Lax"
    s = s.lower().strip()
    if s in ("strict", "lax", "none"):
        return s.capitalize()
    if "no" in s:  # "no same-site" → None
        return "None"
    return "Lax"


def _cookie_from_dict(d: Dict[str, Any]) -> Cookie:
    """Build a Cookie from a generic dict (CDP / Playwright / AdsPower)."""
    return Cookie(
        name=str(d.get("name", "")),
        value=str(d.get("value", "")),
        domain=str(d.get("domain", "") or d.get("host", "") or ""),
        path=str(d.get("path", "/")),
        expires=float(d.get("expires") or d.get("expirationDate") or -1),
        http_only=bool(d.get("httpOnly") or d.get("http_only") or False),
        secure=bool(d.get("secure", False)),
        same_site=_normalize_samesite(d.get("sameSite") or d.get("same_site")),
    )


