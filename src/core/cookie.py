def export_cookies_netscape(cookies: List[Cookie]) -> str:
    """Serialise cookies back into Netscape ``cookies.txt`` format."""
    lines = [
        "# Netscape HTTP Cookie File",
        "# Generated by antidetect-local",
        "# https://curl.haxx.ru/rfc/cookie_spec.html",
        "",
    ]
    for c in cookies:
        domain = c.domain or ""
        flag = "TRUE"  # we always include subdomains
        path = c.path or "/"
        secure = "TRUE" if c.secure else "FALSE"
        exp = int(c.expires) if c.expires and c.expires > 0 else 0
        # Netscape convention: 0 = session cookie
        lines.append("\t".join([domain, flag, path, secure, str(exp), c.name, c.value]))
    return "\n".join(lines) + "\n"


def export_cookies_json(cookies: List[Cookie]) -> str:
    """Serialise cookies to Playwright JSON format."""
    return json.dumps([c.to_playwright() for c in cookies], indent=2, ensure_ascii=False)


# ---------------------------------------------------------------------------
# Convenience: round-trip via path
# ---------------------------------------------------------------------------


def import_cookies(path: Union[str, Path]) -> List[Cookie]:
    """Auto-detect format by extension and parse.

    - ``.txt`` → Netscape
    - ``.json`` → Playwright/CDP JSON
    - ``.adb`` / ``.zip`` / ``.tar.gz`` / directory → AdsPower bundle
    """
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(p)
    if p.is_dir() or p.suffix.lower() in (".adb", ".zip", ".tar", ".tgz"):
        return import_adspower_profile(p)
    if p.suffix.lower() == ".json":
        return import_cookies_json(p.read_text(encoding="utf-8"))
    # default: Netscape
    return import_cookies_netscape(p.read_text(encoding="utf-8"))